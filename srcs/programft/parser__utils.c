#include "../../includes/program.h"

int	valid_char(char c)
{
	if (c != '1'
		&& c != '0'
		&& c != ' '
		&& c != 'N'
		&& c != 'S'
		&& c != 'E'
		&& c != 'W')
		return (0);
	return (1);
}
